v1 = 10 # объем первого тела
m1 = 5 # масса первого тела
v2 = 15 # объем второго тела
m2 = 6 # масса второго тела

density1 = m1 / v1 # плотность первого тела
density2 = m2 / v2 # плотность второго тела

if density1 > density2:
    print("Плотность первого тела больше")
else:
    print("Плотность второго тела больше")
